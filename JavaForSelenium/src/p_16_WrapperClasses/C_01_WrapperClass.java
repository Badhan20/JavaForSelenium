package p_16_WrapperClasses;

public class C_01_WrapperClass {

	public static void main(String[] args) {
		
		//a is a variable, int is data type and 100 is value of a
		int a=100;
		
		System.out.println("Primitive Data Type Value is: "+a);
		
		//b is a reference/object of Integer class with value 200
		Integer b=200;
		
		System.out.println("Wrapper class object value is: "+b);

	}

}
